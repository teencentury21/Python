a = " Hello, World! "
#Upper Case
print(a.upper()) # HELLO, WORLD!

#Lower Case
print(a.lower()) # hello, world!

#Remove Whitespace
print(a.strip()) # returns "Hello, World!"

#Replace String
print(a.replace("H", "J")) # Jello, World!

#Split String
print(a.split(",")) # returns ['Hello', ' World!']